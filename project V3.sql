CREATE DATABASE Airline_system

create table Passenger(
passengerID int primary key,
PEmail varchar(20),
PGender varchar(6),
Pphone_Number int,
PFname varchar(10),
PMname varchar(10),
PLname varchar(10),
PNational_ID int not null,
PBirthdate date ,
);

use Airline_system;

 create table Passenger_PhoneNo(
 passengerID int,
 Pphone_Number int
 primary key(passengerID,Pphone_Number)
 foreign key (passengerID) references Passenger(passengerID)
 ); 
 -- composite primary key

 create table Flight(
 flight_ID int primary key,
 Total_Seats int not null default 30,
 Available_Seats int,
 Forigin varchar(20) not null,
 FDestination varchar(20) not null,
 FArrival_Time time,
 FDeprature_Time time,
 Flight_Status varchar(10),
  );
  Create table seat(
  Seat_ID int primary key,
  IsAvailable char(1) DEFAULT 'Y',
  Booking_ID int,
  Seat_Number int not null,
  Seat_Class varchar(8) not null,
  flight_ID int,
  foreign key (flight_ID) references Flight(flight_ID),
  );
   create table Ticket(
 Ticket_ID int primary key,
 Torigin varchar(20) not null,
 TDestination varchar(20) not null,
 TArrival_Time time,
 TDeparture_Time time,
 Tcompany_Name varchar(10),
 Fname varchar(10),
 Mname varchar(10),
 Lname varchar(10),
 passengerID int ,
 flight_ID int,
 Seat_ID int,
 foreign key (passengerID) references Passenger(passengerID),
 foreign key (flight_ID) references Flight(flight_ID),
 );
  create table Booking(
  Booking_ID int primary key,
  Seat_Number int not null,
  BookingStatus varchar(20) not null,
  Booking_Date date,
  flight_ID int,
  passengerID int,
  PaymenyID int,
 foreign key (passengerID) references Passenger(passengerID),
 foreign key (flight_ID) references Flight(flight_ID),
  );
 Create Table Payment(
 PaymentID int primary key,
 Payment_Method varchar(10),
 Payment_Amount int ,
 Payment_Date date,
 Payment_Status varchar(10),
 Booking_ID int,
 passengerID int,
 foreign key (passengerID) references Passenger(passengerID),
 foreign key (Booking_ID) references Booking(Booking_ID),
 );
create table Is_Owned_By
(
 passengerID int PRIMARY KEY,
 Ticket_ID INT,
 foreign key (Ticket_ID) references Ticket(ticket_ID), 
); 
Create table Belongs_To(
 Ticket_ID INT primary key,
 flight_ID int,
 foreign key (Ticket_ID) references Ticket(ticket_ID), 
);
create table Is_Issued_For(
 Booking_ID int primary key,
 Ticket_ID INT ,
 foreign key (Ticket_ID) references Ticket(ticket_ID), 
);
create table Is_Paid_For(
 PaymentID int primary key,
 Ticket_ID INT ,
 foreign key (Ticket_ID) references Ticket(ticket_ID), 
);
create table Report (
ReportID int primary key,
Report_Type varchar(20),
Generated_Date date,
content varchar(1000),
);
create table Refer(
ReportID int ,
Flight_ID INT ,
primary key(Flight_ID,ReportID)
);
create table Baggage(
BaggageId int primary key,
passengerID int not null,
Ticket_ID int not null,
BWeight int not null,
foreign key (passengerID) references Passenger(passengerID),
foreign key (Ticket_ID) references Ticket(ticket_ID),
);
create table Airport(
AAirport_ID int primary key,
DAirport_ID int not null,
Airport_Location varchar(100),
Airport_Name varchar(100),
country varchar(100),
city varchar(100),
);
create table Operates(
AAirport_ID int,
DAirport_ID int,
Flight_ID INT,
primary key(AAirport_ID,DAirport_ID,Flight_ID )
);
create table Check_In(
passengerID int not null,
BaggageId int primary key ,
foreign key (passengerID) references Passenger(passengerID)
);
create table Airline(
Airline_ID int primary key ,
contact int,
HeadQuarters varchar (100),
AName varchar(100),
);
create table Operates2(
AAirport_ID int,
DAirport_ID int,
Airline_ID int
primary key(AAirport_ID,DAirport_ID,Airline_ID )
);


